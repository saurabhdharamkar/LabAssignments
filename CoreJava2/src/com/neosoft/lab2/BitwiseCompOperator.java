package com.neosoft.lab2;

public class BitwiseCompOperator {

	public static void main(String[] args) {
		
		int a=5;
		int b=7;

		//Bitwise Operator
		
		 System.out.println("a&b = " + (a & b));
		 System.out.println("a|b = " + (a | b));
		 System.out.println("a^b = " + (a ^ b));
		 System.out.println("~a = " + ~a);
	}

}
